public class HW21 {
    //Print odd numbers between 20 and 50 (2 ways)
    public static void main(String[] args) {
        for (int i = 20; i <=50 ; i+=2) {
            System.out.println(i);
        }
        System.out.println("--");
        int a=20;
        while (a<=50){
            System.out.println(a);
            a+=2;
        }
    }
}
