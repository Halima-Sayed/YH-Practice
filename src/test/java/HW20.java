public class HW20 {
    //Print even numbers from 20 to 1 (2 ways)
    public static void main(String[] args) {
        for (int i = 20; i >=2; i-=2) {
            System.out.println(i);
        }
        System.out.println("--");
        int a=20;
        while (a>=2){
            System.out.println(a);
            a-=2;
        }
    }
}
